import React from 'react';
import { myStock } from '../classes/Stock';
import { myCart } from '../classes/cartClass';
import { observer } from 'mobx-react';

const EachProduct = observer(() => {
    return (
        <tbody>
            {
                myStock.products.map(product =>{
                    return(
                        <tr>
                            <td>{product}</td>
                            <td><button type="button" className="btn btn-sm btn-success" onClick={() => buyer(product)}><svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" className="bi bi-bag-check" viewBox="0 0 16 16">
                                <path fillRule="evenodd" d="M10.854 8.146a.5.5 0 0 1 0 .708l-3 3a.5.5 0 0 1-.708 0l-1.5-1.5a.5.5 0 0 1 .708-.708L7.5 10.793l2.646-2.647a.5.5 0 0 1 .708 0z"/>
                                <path d="M8 1a2.5 2.5 0 0 1 2.5 2.5V4h-5v-.5A2.5 2.5 0 0 1 8 1zm3.5 3v-.5a3.5 3.5 0 1 0-7 0V4H1v10a2 2 0 0 0 2 2h10a2 2 0 0 0 2-2V4h-3.5zM2 5h12v9a1 1 0 0 1-1 1H3a1 1 0 0 1-1-1V5z"/>
                                </svg></button></td>
                            <td><button type="button" className="btn btn-sm btn-danger" onClick={() => deleter(product)}><svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" className="bi bi-x-square" viewBox="0 0 16 16">
                                <path d="M14 1a1 1 0 0 1 1 1v12a1 1 0 0 1-1 1H2a1 1 0 0 1-1-1V2a1 1 0 0 1 1-1h12zM2 0a2 2 0 0 0-2 2v12a2 2 0 0 0 2 2h12a2 2 0 0 0 2-2V2a2 2 0 0 0-2-2H2z"/>
                                <path d="M4.646 4.646a.5.5 0 0 1 .708 0L8 7.293l2.646-2.647a.5.5 0 0 1 .708.708L8.707 8l2.647 2.646a.5.5 0 0 1-.708.708L8 8.707l-2.646 2.647a.5.5 0 0 1-.708-.708L7.293 8 4.646 5.354a.5.5 0 0 1 0-.708z"/>
                                </svg></button></td>
                        </tr>
                    )
                })
            }
        </tbody>
    );
})

export default EachProduct;

function deleter(product){
    myCart.addNoBoughtProducts();
    myCart.boughtPercentageNumber();
    myStock.removeProduct(product);
}

function buyer(product){
    myCart.addBoughtProducts();
    myCart.boughtPercentageNumber();
    myStock.removeProduct(product);
}
